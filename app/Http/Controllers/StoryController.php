     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\RedirectResponse
     */
    public function store(Request $request)
    {
        while (Story::where('link',$link = env('APP_URL') . '/s/' . Str::random(40))->first());
        $story = Story::create([
            'link' => $link,
            'user_id' => auth()->id(),
        ]);
        $path = $request->file('media')->storePublicly('stories');
        Media::create([
            'path' => $path,
            'mediaable_id' => $story->id,
            'mediaable_type' => Story::class,
        ]);
        return redirect()->route('profile',auth()->user());
    }

